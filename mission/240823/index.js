let A = 1;
let B = 2;

// 덧셈
let add = A + B;
console.log("덧셈 결과: " + add);

// 뺄셈
let subtract = A - B;
console.log("뺄셈 결과: " + subtract);

// 곱하기
let multiply = A * B;
console.log("곱셈 결과: " + multiply);

// 나누기
let divide = A / B;
console.log("나눗셈 결과: " + divide);

// 나머지
let remainder = A % B;
console.log("나머지 결과: " + remainder);

// 전위 증가
let preIncrement = ++A;
console.log("A: " + A + ", 전위 증가 연산 결과: " + preIncrement);

// 전위 감소
let preDecrement = --A;
console.log("A: " + A + ", 전위 증가 연산 결과: " + preDecrement);

// 후위 증가
let postIncrement = A++;
console.log("A: " + A + ", 전위 증가 연산 결과: " + preIncrement);

// 후위 감소
let postDecrement = A--;
console.log("A: " + A + ", 전위 증가 연산 결과: " + preDecrement);