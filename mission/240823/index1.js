let A = 1;
let B = 2;


// 비교 연산자 ==
let isEqual = A == B;
console.log("값이 같은가요? " + isEqual);

// 비교 연산자 !=
let isNotEqual = A != B;
console.log("다른가요? " + isNotEqual);

// 비교 연산자 ===
let isStrictEqual = A === B;
console.log("타입과 값이 같은가요? " + isStrictEqual);

// 비교 연산자 !==
let isStrictNotEqual = A !== B;
console.log("타입과 값이 다른가요?" + isStrictNotEqual);

// 비교 연산자 >
let isGreaterThan = A > B;
console.log("A가 더 큰가요? " + isGreaterThan);

// 비교 연산자 >=
let isGreaterThanOrEqual = A >= B;
console.log("A가 크거나 같나요? " + isGreaterThanOrEqual);